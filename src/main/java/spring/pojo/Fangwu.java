package spring.pojo;


public class Fangwu {

  private String name;
  private String url;
  private String shouci;
  private String daxiao;
  private String cengshu;
  private String ting;
  private String juli;
  private String money;
  private String sname;

  public String getSname() {
    return sname;
  }

  public void setSname(String sname) {
    this.sname = sname;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }


  public String getShouci() {
    return shouci;
  }

  public void setShouci(String shouci) {
    this.shouci = shouci;
  }


  public String getDaxiao() {
    return daxiao;
  }

  public void setDaxiao(String daxiao) {
    this.daxiao = daxiao;
  }


  public String getCengshu() {
    return cengshu;
  }

  public void setCengshu(String cengshu) {
    this.cengshu = cengshu;
  }


  public String getTing() {
    return ting;
  }

  public void setTing(String ting) {
    this.ting = ting;
  }


  public String getJuli() {
    return juli;
  }

  public void setJuli(String juli) {
    this.juli = juli;
  }


  public String getMoney() {
    return money;
  }

  public void setMoney(String money) {
    this.money = money;
  }

}
